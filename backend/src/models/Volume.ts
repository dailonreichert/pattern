class Volume {
    arestaA: number;
    arestaB: number;
    arestaC: number;
    pi: number;
    raio: number;
    altura: number;

    setArestaA(arestaA: number) {
        this.arestaA = arestaA;
    }

    setArestaB(arestaB: number) {
        this.arestaB = arestaB;
    }
    setArestaC(arestaC: number) {
        this.arestaC = arestaC;
    }
    setPi(pi: number) {
        this.pi = pi;
    }
    setRaio(raio: number) {
        this.raio = raio;
    }
    setAltura(altura: number) {
        this.altura = altura;
    }

}

export default Volume;